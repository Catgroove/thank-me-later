/** Render `text` as block-letter banner rows (no color), trailing whitespace trimmed. */
export function bannerLines(text: string): string[] {
  const glyphs = Array.from(text.toUpperCase(), (ch) => FONT[ch] ?? SPACE);
  const rows: string[] = [];
  for (let r = 0; r < HEIGHT; r += 1) {
    rows.push(
      glyphs
        .map((g) => g[r] ?? "")
        .join(" ")
        .replace(/\s+$/, ""),
    );
  }
  return rows;
}
